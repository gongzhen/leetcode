public class Solution1 {

    public int numSquares(int n) {
        TreeSet<Integer> squreSet = new TreeSet<Integer>();
        int i = 1;
        while(i * i <= n) {
            squreSet.add(i * i);
            i++;
        }
        printSet(squreSet);
        printLine();        
        TreeSet<Integer> subSet = new TreeSet<Integer>();
        int count = 0;
        subSet.add(n);
        while(subSet != null) {
            count++;
            TreeSet<Integer> temp = new TreeSet<Integer>();
            for(int x : subSet) {
                for(int y : squreSet) {
                    if(x == y) {
                        printString(x + " == " + y);
                        return count;
                    } else if (x < y) {
                        break;
                    } 
                    printString(x + " - " + y);
                    temp.add(x - y);
                }
            }
            subSet = temp;
            printSet(subSet);
            printLine();
        }
        printString("count:  " + count);
        return count;
    }

    private void printLine() {
    	System.out.println("---------------------"); 
	}

    private void printString(String arg) {
        System.out.println(arg); 
    }    

    private void printSet(Set<Integer> set) {
        for(int n : set) {
            printString(n + ", ");
        }
    }

	private void printList(int[] list, int length) {
		for(int i = 0; i < length; i ++ ){
			System.out.println(list[i]);
		}
	}    

	public static void main(String[] args) {
		Solution1 obj = new Solution1();
		System.out.println(obj.numSquares(15));
	}

}
